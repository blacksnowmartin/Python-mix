def intro_and_converse():

  print("Hello! I'm a simple Python program designed to chat with you.")
  print("I'm still under development, but I'm learning to be a good conversationalist.")

  name = input("What's your name? ")
  print(f"Nice to meet you, {name}!")

  age = input("How old are you? ")
  print(f"You're {age} years old. That's interesting!")

  hobby = input("What's your favorite hobby? ")
  print(f"I love {hobby} too! It sounds like fun.")

  print("I'm still learning to understand and respond to your conversations better.")
  print("Thanks for chatting with me!")

if __name__ == "__main__":
  intro_and_converse()
